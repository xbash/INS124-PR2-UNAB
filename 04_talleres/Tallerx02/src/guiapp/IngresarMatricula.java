    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
        boolean resultado = true;
        try{
            int Valor_Matricula = Integer.parseInt(txt_valor.getText());
            String texto_carrera=List_Carreras.getSelectedItem().toString();
            
            Matricula m = new Matricula (txt_rut.getText(),txt_nombre.getText(),texto_carrera,Valor_Matricula);
        if (!m.GuardarEnArchivo()){
            JFrame frame = new JFrame("Error");
            JOptionPane.showMessageDialog(frame,"Error al guardar los datos.");
            resultado = false;
        }
            
        }catch (Exception e){
            //Agregar dialogo con error
            JFrame frame = new JFrame("Error");
            JOptionPane.showMessageDialog(frame,"Error al guardar los datos. Error: "+e.getMessage());
            resultado = false;
        }
        if (resultado){
        //Mostrar dialogo success
            JFrame frame = new JFrame("Datos Guardados");
            JOptionPane.showMessageDialog(frame,"Datos Guardados correctamente.");
           txt_rut.setText("");
           txt_nombre.setText("");
           txt_valor.setText("");
        }
    }//GEN-LAST:event_btn_guardarActionPerformed

