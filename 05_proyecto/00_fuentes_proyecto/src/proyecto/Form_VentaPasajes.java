        private Boolean ValidarCantAsientos(int CodigoViaje){
            try{
                ClassMysql MysqlHandler= new ClassMysql();
                String query;
                query = "SELECT PasajesDisponibles from `viajes` WHERE CodigoViaje ='"+CodigoViaje+"'";
                Connection con = MysqlHandler.getCon();
                Statement stm = con.createStatement();
                ResultSet res = stm.executeQuery(query);
                int PasajesDisponibles;
                while (res.next()){
                    PasajesDisponibles = res.getInt("PasajesDisponibles");
                    if (PasajesDisponibles > 0)
                    return true;
                }
            }catch (SQLException e){
                System.out.println(e.getMessage());
            }
            return false; 
        }
        
        
