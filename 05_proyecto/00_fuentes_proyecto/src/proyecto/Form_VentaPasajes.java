    private void LlenarCodigosViajes(){
        cbxCodigoViaje.addItem("Seleccionar Codigo de Viaje");
        try{
            ClassMysql MysqlHandler= new ClassMysql();
                String query;
                query = "select CodigoViaje,destino from `viajes`";
                Connection con = MysqlHandler.getCon();
                Statement stm = con.createStatement();
                ResultSet res = stm.executeQuery(query);
                while (res.next()){
                     cbxCodigoViaje.addItem(res.getString("CodigoViaje") +" - "+ res.getString("destino"));
                }
        }catch (SQLException e){
            System.out.println(e.getMessage());
        }
    }
    
