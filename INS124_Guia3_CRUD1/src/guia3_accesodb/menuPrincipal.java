    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Conexion conexion = new Conexion();
        Connection con = conexion.getConnection();
        try{
            Statement sentencia = con.createStatement();
            int res = sentencia.executeUpdate("INSERT INTO persona VALUES('"+txtClave.getText()+"','"+txtNombre.getText()+"','"+txtTelefono.getText()+"','"+txtFechaNac.getText()+"')");
            JOptionPane.showMessageDialog(null,"El ingreso ha sido satisfactorio");
            if (res > 0){
                JOptionPane.showMessageDialog(null,"Registro ingresado");
                LimpiarCajas();
            } else
                JOptionPane.showMessageDialog(null,"Error registro no ingresado");
            con.close();
        } catch(Exception ex){
            JOptionPane.showMessageDialog(null,"Se ha producido un error al Guardar. Excepcion :"+ex);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        Conexion conexion = new Conexion();
        Connection con = conexion.getConnection();
        try{
            Statement sentencia = con.createStatement();
            resultset = sentencia.executeQuery("select nombre, telefono, fecha_nac from persona where clave = '"+txtClave.getText()+"' ");
            while(resultset.next()){
                String nomb = resultset.getString("nombre");
                txtNombre.setText(nomb);
                String tel = resultset.getString("telefono");
                txtTelefono.setText(tel);
                String fechanac = resultset.getString("fecha_nac");
                txtFechaNac.setText(fechanac);
                JOptionPane.showMessageDialog(null,"La consulta a sido exitosa");
                resultset.close();
            }
        } catch(Exception ex){
            System.out.println("Se ha producido un error al Buscar. Excepcion: "+ex);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

