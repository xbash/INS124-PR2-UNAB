    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        // TODO add your handling code here:
        //Resultset
        Conexion conexion = new Conexion();
        ResultSet res;
        Connection con = conexion.getConnection();
        JOptionPane.showMessageDialog(null,"Salida: ");
        System.out.println("Prueba de boton listar");
        try{
            Statement sentencia = con.createStatement();
            res = sentencia.executeQuery("select * from persona");
            String salida = "";
            salida = "ID_Persona Nombre Telefono FechaNac \n"; //fila de titulo
            JOptionPane.showMessageDialog(null,"Salida : "+salida);
            
            while(res.next()){
                String idclave = res.getString("clave");
                String nomb = res.getString("nombre");
                String tel = res.getString("telefono");
                String fnac = res.getString("fecha_nac");
                salida += idclave +" "+nomb+" "+tel+" "+fnac+"\n";
            }
            JOptionPane.showMessageDialog(null,"Salida : "+salida);
            sentencia.close();
            con.close();
            res.close();
        } catch(Exception ex){
            System.out.println("Se ha producido un error al Listar. Excepcion: "+ex);
        }
    }//GEN-LAST:event_btnListarActionPerformed

