private void LlenarTabla(String Mes){
        ClassMysql MysqlHandler= new ClassMysql();
        Connection con = MysqlHandler.getCon();
	//String selectString = "select * from `view_VentasMensuales` WHERE `Mes`LIKE '%"+Mes+"'"; //Falta El Subtotal
        String selectString="(select Folio, CodigoViaje, FechaVta, Rut_Vendedor, Precio , Mes\n" +
            "from `view_VentasMensuales`  where mes LIKE '%"+Mes+"')\n" +
            "UNION\n" +
            "(SELECT NULL,CodigoViaje,NULL,'Subtotal', CodigoViaje,SUM(precio) as Total from `view_VentasMensuales` where mes LIKE '%"+Mes+"' GROUP BY CodigoViaje)  \n" +
            "ORDER BY CodigoViaje,Folio DESC";
        System.out.println(selectString);
        Object columnNames[] = { "Folio", "CodigoViaje", "FechaVta", "Rut_Vendedor","Precio","Mes"};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);
        String [] Row = new String[6];
        try {
			Statement stmt = con.createStatement();
			ResultSet rs = stmt.executeQuery(selectString);
			while (rs.next()) {
                           Row[0] = String.valueOf(rs.getInt("Folio"));
                           Row[1] = String.valueOf(rs.getInt("CodigoViaje"));
                           Row[2] = rs.getString("FechaVta");
                           Row[3] = rs.getString("Rut_Vendedor");
                           Row[4] = String.valueOf(rs.getInt("Precio"));
                           Row[5] = rs.getString("Mes");
                           
                           if (rs.getInt("Folio") == 0){
                               Row[0]="";
                               Row[1]="";
                               Row[2]="";
                               Row[3]="";
                               Row[4]="SubTotal del mes";
                               Row[5] = rs.getString("Mes");
                           }
                           model.addRow(Row);
			}
			stmt.close();
			con.close();                      
                        tblResultado.setModel(model);
		} catch(SQLException ex) {
			System.err.println("SQLException: " + ex.getMessage());
		}
    }

